# Print a classic greeting
print("Hello, World!")

# Assign values to variables
num1 = 10
num2 = -5

# Perform an arithmetic operation
sum_of_numbers = num1 + num2

# Print the result using an f-string (formatted string literal)
print(f"The sum of {num1} and {num2} is: {sum_of_numbers}")

# Demonstrate a conditional statement
if sum_of_numbers > 10:
    print("The sum is greater than 10.")
else:
    print("The sum is not greater than 10.")
